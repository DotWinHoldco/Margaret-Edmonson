const mobileItems = navItems
  .filter((item) => item.mobileOrder > 0)
  .sort((a, b) => a.mobileOrder - b.mobileOrder)

interface AdminSidebarProps {
  user: {
    name: string
    email: string
    avatar_url?: string | null
  } | null
}

export default function AdminSidebar({ user }: AdminSidebarProps) {
  const pathname = usePathname()
  const router = useRouter()

  const isActive = (href: string) => {
    if (href === '/admin') return pathname === '/admin'
    return pathname.startsWith(href)
  }

  const handleSignOut = async () => {
    const supabase = createBrowserClient(
      process.env.NEXT_PUBLIC_SUPABASE_URL!,
      process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!
    )
    await supabase.auth.signOut()
    router.push('/login')
  }

  return (
    <>
      {/* Desktop sidebar */}
      <aside className="hidden lg:flex lg:flex-col lg:w-64 lg:fixed lg:inset-y-0 bg-charcoal text-cream/80">
        {/* Logo */}
        <div className="flex items-center gap-3 px-6 py-5 border-b border-cream/10">
          <div className="w-9 h-9 rounded-lg bg-teal flex items-center justify-center">
            <span className="font-display text-lg font-bold text-cream">A</span>
          </div>
          <div>
            <p className="font-display text-lg font-semibold text-cream leading-tight">ArtByME</p>
            <p className="text-xs text-cream/40 font-body">Admin Panel</p>
          </div>
        </div>

        {/* Navigation */}
        <nav className="flex-1 overflow-y-auto px-3 py-4 space-y-1">
          {navItems.map((item) => {
            const active = isActive(item.href)
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-body transition-colors ${
                  active
                    ? 'bg-teal text-cream font-medium'
                    : 'text-cream/60 hover:bg-cream/5 hover:text-cream'
                }`}
              >
                {item.icon}
                <span>{item.label}</span>
              </Link>
            )
          })}
        </nav>

        {/* User & Sign out */}
        <div className="border-t border-cream/10 px-4 py-4">
          {user && (
            <div className="flex items-center gap-3 mb-3 px-2">
              {user.avatar_url ? (
                <img
                  src={user.avatar_url}
                  alt={user.name}
                  className="w-8 h-8 rounded-full object-cover"
                />
              ) : (
                <div className="w-8 h-8 rounded-full bg-teal/30 flex items-center justify-center">
                  <span className="text-sm font-medium text-cream">
                    {user.name?.charAt(0)?.toUpperCase() || 'U'}
                  </span>
                </div>
              )}
              <div className="min-w-0">
                <p className="text-sm font-medium text-cream truncate">{user.name}</p>
                <p className="text-xs text-cream/40 truncate">{user.email}</p>
              </div>
            </div>
          )}
          <button
            onClick={handleSignOut}
            className="flex items-center gap-3 w-full px-3 py-2.5 rounded-lg text-sm font-body text-cream/50 hover:bg-cream/5 hover:text-cream transition-colors"
          >
            <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0 0 13.5 3h-6a2.25 2.25 0 0 0-2.25 2.25v13.5A2.25 2.25 0 0 0 7.5 21h6a2.25 2.25 0 0 0 2.25-2.25V15m3 0 3-3m0 0-3-3m3 3H9" />
            </svg>
            <span>Sign Out</span>
          </button>
        </div>
      </aside>

      {/* Mobile bottom tab bar */}
      <nav className="lg:hidden fixed bottom-0 inset-x-0 z-50 bg-charcoal border-t border-cream/10 safe-area-pb">
        <div className="flex items-center justify-around px-2 py-2">
          {mobileItems.map((item) => {
            const active = isActive(item.href)
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex flex-col items-center gap-1 px-3 py-1.5 rounded-lg transition-colors ${
                  active
                    ? 'text-teal'
                    : 'text-cream/50 hover:text-cream'
                }`}
              >
                {item.icon}
                <span className="text-[10px] font-body">{item.label}</span>
              </Link>
            )
          })}
        </div>
      </nav>
    </>
  )
}
